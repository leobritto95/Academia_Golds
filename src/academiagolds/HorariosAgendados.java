    private void Excluir() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum agendamento selecionado");
        } else {
            try {
                con = BancoDeDados.getConexao();
                //cria a string para inserir no banco
                String query = "DELETE FROM agend_pro_aluno_recep WHERE dataHora = ?";

                PreparedStatement cmd;
                cmd = con.prepareStatement(query);

                String index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 2);
                cmd.setString(1, index);

                cmd.executeUpdate();
            } catch (SQLException ex) {
                System.out.println("Erro de SQL " + ex.getMessage());
            }
        }
    }

