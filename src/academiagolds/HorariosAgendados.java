    public void carregaTable() {
        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String combo = "";

            if (JCBant.isSelected()) {
                combo = " AND (dataHora >= ?)";
            }

            String query = "SELECT * FROM agend_pro_aluno_recep JOIN professor ON fk_Professor_Cod = professor.cod JOIN aluno ON fk_Aluno_Nmatricula = Nmatricula  AND (dataHora BETWEEN ? AND ?)" + combo;

            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            String data = null;
            String data2 = null;
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            if (!(jDateIni1.getDate() == null || jDateFim1.getDate() == null)) {
                data = sdf.format(jDateIni1.getDate());
                data2 = sdf.format(jDateFim1.getDate());
            } else {
                data = "0001-01-01";
                data2 = "9999-01-01";
            }

            cmd.setString(1, data);
            cmd.setString(2, data2);
            if (JCBant.isSelected()) {
                Date hoje = new Date(System.currentTimeMillis());
                String data3 = sdf.format(hoje);
                cmd.setString(3, data3);
            }
            ResultSet rs;

            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("aluno.nome"), rs.getString("professor.nome"), rs.getString("dataHora")});
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }
