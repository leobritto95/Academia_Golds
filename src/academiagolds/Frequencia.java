    private void Excluir() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado");
        } else {
            try {
                con = BancoDeDados.getConexao();
                //cria a string para inserir no banco
                String query = "DELETE FROM autoriza WHERE fk_Aluno_Nmatricula = ? AND dataHora= ?";

                PreparedStatement cmd;
                cmd = con.prepareStatement(query);

                cmd.setString(1, id);
                String index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                cmd.setString(2, index);
                cmd.executeUpdate();
            } catch (SQLException ex) {
                System.out.println("Erro de SQL " + ex.getMessage());
            }
        }
    }

