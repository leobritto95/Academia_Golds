    public void carregaTable(String proc) {
        String combo = "";
        if (!(jDateIni.getDate() == null || jDateFim.getDate() == null)) {
            combo = " AND (dataHora BETWEEN ? AND ?)";
        }
        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String query = "SELECT * FROM autoriza JOIN aluno ON fk_Aluno_Nmatricula = Nmatricula JOIN recepcionista ON fk_Recepcionista_Cod = cod WHERE (fk_Aluno_Nmatricula = ?)" + combo;

            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            cmd.setString(1, proc);
            if (!(jDateIni.getDate() == null || jDateFim.getDate() == null)) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String data = sdf.format(jDateIni.getDate());
                String data2 = sdf.format(jDateFim.getDate()) + " 23:59:59";
                cmd.setString(2, data);
                cmd.setString(3, data2);
            }

            ResultSet rs;

            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("dataHora"), rs.getString("Recepcionista.nome")});
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }

