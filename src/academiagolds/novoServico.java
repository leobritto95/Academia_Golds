    private void Envia() {
        if (Up) {
            try {
                con = BancoDeDados.getConexao();

                //cria a string para inserir no banco
                String query = "UPDATE servico SET modalidade = ?, valor = ? WHERE ID = ?";

                // Cria o comando
                PreparedStatement stmt = con.prepareStatement(query);
                //seta os valores inseridos no banco
                stmt.setString(1, Lnome.getText());
                stmt.setString(2, Lvalor.getText());
                stmt.setInt(3, id);

                //executa o comando no banco de dados
                stmt.executeUpdate();

                //fecha os comandos de conex√£o
                stmt.close();
                // con.close();

                this.dispose();

            } catch (SQLException ex) {
                System.out.println(ex);
                JOptionPane.showMessageDialog(null, "Valor Invalido");
            }
        } else {
            try {
                con = BancoDeDados.getConexao();
                //cria a string para inserir no banco
                String queryV = "SELECT * from servico WHERE modalidade = ? ";

                // Cria o comando
                PreparedStatement verif = con.prepareStatement(queryV);
                verif.setString(1, Lnome.getText());
                ResultSet rs = verif.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Modalidade ja cadastrada");
                } else {
                    //cria a string para inserir no banco
                    String query = "INSERT INTO servico (modalidade,valor) VALUES(?,?)";

                    // Cria o comando
                    PreparedStatement stmt = con.prepareStatement(query);

                    //seta os valores inseridos no banco
                    stmt.setString(1, Lnome.getText());
                    stmt.setString(2, Lvalor.getText());

                    //executa o comando no banco de dados
                    stmt.executeUpdate();

                    //fecha os comandos de conex√£o
                    stmt.close();
                    this.dispose();
                }
                verif.close();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Dados(s) Invalidos(s)");
                System.out.println(ex);
            }
        }
    }
