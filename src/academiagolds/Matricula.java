    public void carregaTable(String proc) {
        if (jDateIni1.getDate() == null || jDateFim1.getDate() == null) {
            carregaTable(proc, "0001-01-01", "9999-01-01");
        } else {
            procurarData();
        }
    }

    public void carregaTable(String proc, String dataIni, String dataFim) {
        String combo = "";
        if (!JCBexp.isSelected()) {
            combo = " AND (dataFim >= ?)";
        }
        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String query = "SELECT * FROM _matricula JOIN servico ON fk_Servico_Id = Id WHERE (fk_Aluno_Nmatricula = ?) AND (dataInicio BETWEEN ? AND ?)" + combo;
            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            cmd.setString(1, proc);
            cmd.setString(2, dataIni);
            cmd.setString(3, dataFim);
            if (!JCBexp.isSelected()) {
                Date hoje = new Date(System.currentTimeMillis());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String data = sdf.format(hoje);
                cmd.setString(4, data);
            }
            ResultSet rs;

            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("dataInicio"), rs.getString("modalidade"), rs.getString("dataFim"), rs.getString("valor")});
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }
