    private void Excluir() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhuma matricula selecionada");
        } else {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "A Exclusão da Matricula resultará na Exclusão\n dos seus dados relacinados \n Deseja continuar?");
            if (showConfirmDialog == 0) {
                try {
                    con = BancoDeDados.getConexao();
                    //cria a string para inserir no banco
                    String query = "DELETE FROM _matricula WHERE (fk_Aluno_Nmatricula = ?) AND (fk_Servico_Id = (SELECT id FROM SERVICO WHERE modalidade = ?)) AND (dataInicio = ?)";

                    PreparedStatement cmd;
                    cmd = con.prepareStatement(query);

                    cmd.setString(1, id);
                    String index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 1);
                    cmd.setString(2, index);
                    index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    cmd.setString(3, index);
                    cmd.executeUpdate();
                } catch (SQLException ex) {
                    System.out.println("Erro de SQL " + ex.getMessage());
                }
            }
        }
    }

