    private void Editar() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum aluno selecionado");
        } else {
            String id = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            cadastroAluno dialog = new cadastroAluno(this, rootPaneCheckingEnabled, id);
            dialog.setVisible(true); //Chama a dialog
            carregaTable(Tproc.getText());
        }
    }

    private void Excluir() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum aluno selecionado");
        } else {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "A Exclusão do Aluno resultará na Exclusão\n dos seus dados relacinados \n Deseja continuar?");
            if (showConfirmDialog == 0) {
                try {
                    con = BancoDeDados.getConexao();
                    //cria a string para inserir no banco
                    String query = "DELETE FROM aluno WHERE nMatricula = ?";

                    PreparedStatement cmd;
                    cmd = con.prepareStatement(query);
                    String index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    cmd.setString(1, index);
                    cmd.executeUpdate();
                    carregaTable(Tproc.getText());
                } catch (SQLException ex) {
                    System.out.println("Erro de SQL " + ex.getMessage());
                }
            }
        }
    }

