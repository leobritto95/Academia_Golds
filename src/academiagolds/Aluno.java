    public void carregaTable(String proc) {
        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String query = "SELECT * FROM aluno WHERE nome LIKE ? OR nmatricula = ? OR CPF = ? OR RG = ?";

            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            cmd.setString(1, "%" + proc + "%");
            cmd.setString(2, proc);
            cmd.setString(3, proc);
            cmd.setString(4, proc);

            ResultSet rs;

            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("nmatricula"), rs.getString("nome"), rs.getString("CPF"), rs.getString("RG"), rs.getString("Tel"), rs.getString("Sexo")});
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }

