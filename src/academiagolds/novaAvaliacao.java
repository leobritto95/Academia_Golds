private void comboCarrega() {

        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String query = "SELECT * FROM professor";

            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            ResultSet rs;

            rs = cmd.executeQuery();

            while (rs.next()) {
                JCprof.addItem(rs.getString("Cod") + "-" + rs.getString("nome"));
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }

    private void Enviar() {
        try {
            con = BancoDeDados.getConexao();

            //cria a string para inserir no banco
            String query = "INSERT INTO avalia (fk_Aluno_Nmatricula,fk_Professor_Cod,treino,parecer,dataAvaliacao) VALUES(?,?,?,?,?)";

            // Cria o comando
            PreparedStatement stmt = con.prepareStatement(query);

            //seta os valores inseridos no banco
            stmt.setString(1, id);
            String aux = JCprof.getSelectedItem().toString();
            aux = aux.substring(0, aux.indexOf("-"));
            stmt.setString(2, aux);
            stmt.setString(3, JTtreino.getText());//trocar dps  
            stmt.setString(4, Jtparece.getText());
            Date data = new Date(System.currentTimeMillis());
            stmt.setDate(5, data);

            //executa o comando no banco de dados
            stmt.executeUpdate();

            //fecha os comandos de conex√£o
            stmt.close();
            this.dispose();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Dados(s) Invalidos(s)");
            System.out.println(ex);
        }
    }

