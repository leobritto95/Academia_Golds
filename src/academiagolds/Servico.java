    private void Excluir() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum serviÃ§o selecionado");
        } else {
            int showConfirmDialog = JOptionPane.showConfirmDialog(this, "A ExclusÃ£o do Servico resultarÃ¡ na ExclusÃ£o\n dos seus dados relacinados \n Deseja continuar?");
            if (showConfirmDialog == 0) {
                try {
                    con = BancoDeDados.getConexao();
                    //cria a string para inserir no banco
                    String query = "DELETE FROM servico WHERE ID = ?";

                    PreparedStatement cmd;
                    cmd = con.prepareStatement(query);
                    String index = (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                    cmd.setString(1, index);
                    cmd.executeUpdate();
                    carregaTable();
                } catch (SQLException ex) {
                    System.out.println("Erro de SQL " + ex.getMessage());
                }
            }
        }
    }

    private void Editar() {
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum serviÃ§o selecionado");
        } else {

            ArrayList<String> dados = new ArrayList<>();
            for (int i = 0; i < jTable1.getColumnCount(); i++) {
                dados.add((String) jTable1.getValueAt(jTable1.getSelectedRow(), i));
            }

            novoServico dialog = new novoServico(null, rootPaneCheckingEnabled, dados);
            dialog.setVisible(true); //Chama a dialog
            carregaTable();
        }
    }
