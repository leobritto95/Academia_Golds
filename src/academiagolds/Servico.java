    public void carregaTable() {
        try {
            con = BancoDeDados.getConexao();
            //cria a string para inserir no banco
            String query = "SELECT * FROM servico WHERE modalidade LIKE ?";

            PreparedStatement cmd;
            cmd = con.prepareStatement(query);
            cmd.setString(1, "%" + "" + "%");

            ResultSet rs;

            rs = cmd.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);

            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("ID"), rs.getString("modalidade"), rs.getString("valor")});
            }

        } catch (SQLException ex) {
            System.out.println("Erro de SQL " + ex.getMessage());
        }
    }

